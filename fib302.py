"""2 - Faça um programa que calcule as primeiras 5 linhas das 3 principais séries de linhas do Hidrogenio (m=1,2,3), Lyman, Balmer e Paschen de acordo com a fórmula de Rydberg"""

# Constante de Rydberg (em nm^-1)
R = 1.097e-2

# Séries
series_nomes = {
    1: "Lyman (UV)",
    2: "Balmer (Visível)",
    3: "Paschen (Infravermelho)"
}

print(f"{'Série':<20} | {'Transição (n->m)':<18} | {'Comp. de Onda (nm)':<20}")
print("=" * 65)

# Loop pelas séries m=1, m=2, m=3
for m in [1, 2, 3]:

    # Loop pelas 5 primeiras linhas de cada série
    # As linhas começam sempre em n = m + 1
    for k in range(1, 6):
        n = m + k

        # Fórmula de Rydberg: 1/lambda = R * (1/m² - 1/n²)
        um_sobre_lambda = R * ( (1/(m**2)) - (1/(n**2)) )

        # Invertendo para achar o lambda
        lambda_nm = 1 / um_sobre_lambda

        # Imprimindo formatado
        nome = series_nomes[m] if k == 1 else "" # Mostra o nome apenas na 1ª linha do bloco
        print(f"{nome:<20} | n={n} -> m={m}      | {lambda_nm:.2f} nm")

    print("-" * 65) # Separador entre séries

