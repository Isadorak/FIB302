g = 9.8  # Aceleração da gravidade

# Convertendo para radianos
theta = math.radians(angulo_graus)

# Tempo total de voo: T = 2 * v0 * sin(theta) / g
tempo_total = (2 * v0 * math.sin(theta)) / g

# Altura máxima: H = (v0^2 * sin^2(theta)) / 2g
altura_max = (v0**2 * math.sin(theta)**2) / (2 * g)

# Alcance horizontal: R = (v0^2 * sin(2*theta)) / g
alcance = (v0**2 * math.sin(2 * theta)) / g

print("\n* Parâmetros do Movimento *")
print(f"Tempo total de voo: {tempo_total:.2f} s")
print(f"Altura máxima:      {altura_max:.2f} m")
print(f"Distância (horizontal) percorrida: {alcance:.2f} m")

